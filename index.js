
// github :

// 1 -> git --version
        // checks version of git

// 2 -> git config --global user.name "rahul"
        // set the name of the user

// 3 -> git config --global user.email "0K4dI@example.com"
        // set the email of the user

// 4 -> git init
        // initialize the git   

// 5 -> git status
        // checks the status of git

// 6 -> git add .
        // add all the files to the git

// 7 -> git commit -m "first commit"
        // commit the changes

// 8 -> git log --oneline
        // shows the log of the git

// 9 -> git diff
        // shows the diff of the git

// 10 -> git branch
        // shows the branch of the git

// 11 -> git branch -a
        // shows the branch of the git

// 12 -> git branch new-feature
        // create a new branch

// 13 -> git checkout new-feature
        // switch to the new branch

// 14 -> git checkout master
        // switch to the master branch

// 15 -> git merge new-feature
        // merge the new branch

// 16 -> git push origin main
        // push the changes to the remote

// 17 -> git pull origin main
        // pull the changes from the remote

// 18 -> git branch -d new-branch
        // delete the branch